from __future__ import annotations

import argparse
from pathlib import Path

from .out_of_time_eval import run_out_of_time_recommendation_check


def _parse_str_list(text: str) -> list[str]:
    items = [token.strip() for token in text.split(",") if token.strip()]
    if not items:
        raise ValueError("Expected at least one item.")
    return items


def main() -> None:
    parser = argparse.ArgumentParser(description="Evaluate recommended regional models on timestamp-based out-of-time split.")
    parser.add_argument(
        "--input-dir",
        default="/Users/seoki/Desktop/research/outputs/noaa_same_area_pooled_benchmark_61day_2026-03-18_leakfix",
        help="Directory containing <region>_pooled_pairwise.csv files.",
    )
    parser.add_argument("--output-root", required=True, help="Output directory for out-of-time evaluation artifacts.")
    parser.add_argument("--regions", default="houston,nola,seattle", help="Comma-separated region names.")
    parser.add_argument(
        "--recommendation-csv",
        default="/Users/seoki/Desktop/research/outputs/2026-04-04_all_models_seed_sweep_expanded/all_models_seed_sweep_recommendation.csv",
        help="Recommendation CSV generated by all_models_seed_sweep.",
    )
    parser.add_argument(
        "--baseline-leaderboard-csv",
        default="/Users/seoki/Desktop/research/outputs/2026-04-04_all_models_multiarea_expanded/all_models_multiarea_leaderboard.csv",
        help="Baseline leaderboard CSV for delta calculation.",
    )
    parser.add_argument("--split-strategy", default="timestamp", choices=["timestamp"], help="Out-of-time split strategy.")
    parser.add_argument("--train-fraction", type=float, default=0.6, help="Train fraction.")
    parser.add_argument("--val-fraction", type=float, default=0.2, help="Validation fraction.")
    parser.add_argument("--torch-device", default="auto", help="Torch device.")
    parser.add_argument("--random-seed", type=int, default=42, help="Random seed.")
    parser.add_argument("--include-regional-cnn", action="store_true", help="Also run CNN family during out-of-time benchmark.")
    parser.add_argument("--cnn-losses", default="weighted_bce,focal", help="Comma-separated CNN losses.")
    parser.add_argument("--min-positive-support", type=int, default=10, help="Minimum positive support warning threshold.")
    args = parser.parse_args()

    input_dir = Path(args.input_dir).resolve()
    regions = _parse_str_list(args.regions)
    input_paths = {region: input_dir / f"{region}_pooled_pairwise.csv" for region in regions}
    cnn_losses = _parse_str_list(args.cnn_losses)

    summary = run_out_of_time_recommendation_check(
        input_paths_by_region=input_paths,
        recommendation_csv_path=args.recommendation_csv,
        output_root=args.output_root,
        baseline_leaderboard_csv_path=args.baseline_leaderboard_csv,
        split_strategy=args.split_strategy,
        train_fraction=float(args.train_fraction),
        val_fraction=float(args.val_fraction),
        torch_device=args.torch_device,
        random_seed=int(args.random_seed),
        include_regional_cnn=bool(args.include_regional_cnn),
        cnn_losses=cnn_losses,
        min_positive_support=int(args.min_positive_support),
        auto_adjust_split_for_support=False,
    )

    print(f"summary_json={summary['summary_json_path']}")
    print(f"results_csv={summary['results_csv_path']}")
    print(f"results_md={summary['results_md_path']}")


if __name__ == "__main__":
    main()
